function setup() {
    cnv = new Canvas(800,  800);
    var x = 40;
    var y = 0;
    for (x = 0; x < 10; x++) {
        for (y = 0; y < 10; y++) {
            var block = new Sprite(x*80 + 40, y*80 + 40, 40, 50);
            block.color = color(255-y*20, 0, 0);
        }
    }
}

